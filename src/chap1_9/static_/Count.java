package chap1_9.static_;

public class Count {
    /*
        접근 제한자 (access modifier) - 클래스, 필드, 메서드, 생성자
        사용 제한자 (usage modifier)
         static - 필드, 메서드, 내부클래스 (일반 클래스 x)
         final - 클래스, 필드, 메서드
         abstract - 클래스, 메서드
     */

    // static - 정적, 공유됨, 객체가 필요없음
    public static int x;      //  정적 필드     - 공유 필드
    public int y;             //  인스턴스 필드 - 객체에 묶인 필드


}
